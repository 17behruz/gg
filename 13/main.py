import pandas as pd


data = {
    'Фамилия': ['Иванов', 'Петров', 'Сидоров', 'Козлов', 'Смирнова'],
    'Зарплата': [35000, 45000, 28000, 55000, 42000],
    'Стаж': [5, 12, 9, 15, 11],
    'Должность': ['Менеджер', 'Инженер', 'Клерк', 'Директор', 'Бухгалтер']
}


df = pd.DataFrame(data)

average_salary = df['Зарплата'].mean()

workers_below_avg_salary = df[df['Зарплата'] < average_salary]


workers_over_10_years = df[df['Стаж'] > 10]


print("Работники с зарплатой ниже средней:")
print(workers_below_avg_salary[['Фамилия', 'Зарплата']])

print("\nРаботники с более чем 10-летним стажем:")
print(workers_over_10_years[['Фамилия', 'Зарплата', 'Стаж', 'Должность']])
